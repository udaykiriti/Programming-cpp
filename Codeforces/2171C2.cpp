#define vi vector<int>
#define endl '\n'

#define LeadingZeroes(x) __builtin_clz((unsigned)(x))

void solve(){
   //soLuSHoNN hErE.........
   int n;
   cin >> n;
   vi vec1(n),vec2(n);
   for(auto &x : vec1) cin >> x;
   for(auto &x : vec2) cin >> x;

   vi res(n);
   int tmp(0);
   for(int i = 0 ; i < n ; i++){
    res[i] = vec1[i] ^ vec2[i];
    tmp ^= res[i];
   }

   if(tmp == 0) {
    cout <<"Tie" << '\n';
    return;
   }

   int tmp2 = 31 - LeadingZeroes(tmp);

   int end = -1;

   for(int i = 0 ; i < n ; i++){
    if((res[i] >> tmp2) & 1) 
        end = i;
   }

   cout << (end % 2 == 0 ? "Ajisai" : "Mai") << endl;

}

