#define ll long long 

vector<ll> g[200001];
ll a[200001],OtoZ[200001],ZtoO[200001],ans;
bool vis[200001],b[200001],c[200001];


void dfs(ll u , bool ok , ll val){
    if(!ok){
        vis[u] = 1;
        if(b[u] and !c[u]) OtoZ[u]++;
        if(!b[u] and c[u]) ZtoO[u]++;

        for(ll i = 0 ; i < (ll)g[u].size() ; i++){
            ll v = g[u][i];
            if(!vis[v]){
                dfs(v,ok,val);
                OtoZ[u] += OtoZ[v];
                ZtoO[u] += ZtoO[v];
            }
        }
    }
    else{
		if(val>a[u]){
			ans-=(2*min(OtoZ[u],ZtoO[u])*val);
			ans+=(2*min(OtoZ[u],ZtoO[u])*a[u]);
		}
		vis[u]=0;
		for(ll i=0;i<(ll)g[u].size();i++){
			ll v=g[u][i];
			if(vis[v]){
				dfs(v,ok,min(val,a[u]));
			}
		}
	}
}

void solve(){
   //soLuSHoNN hErE.........
   ll n;
   cin >> n;
   for(ll i = 1 ; i <= n ; i++) cin >> a[i] >> b[i] >> c[i];
   for(ll i = 1 ; i < n ; i++){
    ll x,y;
    cin >> x >> y;
    g[x].push_back(y);
    g[y].push_back(x);
   }
   //for(int i = 1 ; i < n ; i++) debug(g[i]);
    dfs(1,0,-1);
   if(OtoZ[1]!=ZtoO[1]){
		cout << -1 << endl;
		return;
    }
	ans = (OtoZ[1]+ZtoO[1])*a[1];
	dfs(1,1,a[1]);
	cout << ans << '\n';
}

