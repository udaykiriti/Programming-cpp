void precom(){
  for(ll i = 1 ;i <= m; i++){
    dp[0][i] = LLONG_MAX;
  }
  dp[0][1] = 0;
  
  for(ll i = 1 ;i <= n; i++){
    ans[0] = LLONG_MAX;
  }
}

void __GO(){
  //soLuSHoNN hErE.........
  //int n,m,k;
  cin >> n >> m >> k;
  precom();

  
  for(ll i = 1 ;i <= n; i++){
    for(ll j = 1; j <= m ; j++){
      cin >> a[i][j];
    }
  }

  for(ll i = 1 ; i <= n ; i++){
    for(ll j = 1 ; j <= m ;j++){

      ans[j] = min(ans[j - 1] , dp[i -1][j] )+ a[i][j];
      dp[i][j] = ans[j];
    }

    for(ll l = 1; l < m ; l++){

      ll flag = a[i][1];

      for(ll j = 1 ; j < m ; j++){
        a[i][j] = a[i][j + 1];
      }

      a[i][m] = flag;

      for(ll j = 1 ; j <= m ; j++){
        ans[j] = min(dp[i-1][j] , ans[j-1]) + a[i][j];
        ll res = ans[j] + l * k;
        dp[i][j] = min(dp[i][j] , res);
      }
    }
  }
  cout << dp[n][m] << endl;
}


