void solve() {
    ll h, d;
    cin >> h >> d;
    ll lo = d, hi = 2 * d - 1, ans = 2 * d;
    while (lo <= hi) {
        ll mid = lo + (hi - lo) / 2;
 
        ll x = mid - d + 1;
        ll s1 = (d / x) * (d / x + 1) / 2;
        ll s2 = (d / x + 1) * (d / x + 2) / 2;
        ll s = (d % x) * s2 + (x - d % x) * s1;

        if (s < h + mid - d) {
            ans = mid;
            hi = mid - 1;
        } else
            lo = mid + 1;
    } 
    cout << ans << '\n';
}


