using i64 = long long;
const int inf = 2e9 + 5;
#define vi vector<int>
#define pb push_back


void solve(){
   //soLuSHoNN hErE.........
    int n;
    cin >> n;
    vector<vi> g(n + 5);
    for (int i = 2; i <= n; i++) {
        int u, v;
        cin >> u >> v;
        
        g[u].pb(v);
        g[v].pb(u);
    }

    set<pair<int, int>> se;
    // store all exst edgs As ordd prs (u,v)..
    for (int i = 1 ; i <= n; i++) {
        for (auto& x : g[i])
            se.insert({i, x}); // (i -> x)
    }
    vector<vi> v;

    for (int i = 1; i <= n; i++) 
        v.pb({ (int)g[i].size(), i });
    sort(v.begin(), v.end(), greater<>());
    // here v[0] has vertex with hight DeGree

    int ans = 0;
    for (size_t i = 0; i < v.size(); i++) {
        for (size_t j = i + 1; j < v.size(); j++) {
            /* 
            if(a,b) not belongs to E -> ans = deg(a) + deg(b) -1
            if(a,b)  belongs to E -> ans = deg(a) + deg(b) -2
             */
            auto it = se.find({v[i][1], v[j][1]});
            if (it == se.end())
                ans = max(ans, v[i][0] + v[j][0] - 1);
            else 
                ans = max(ans, v[i][0] + v[j][0] - 2);

            // here v is sorted by desc ord ,if evn the curr pair

            if (v[i][0] + v[j][0] - 1 < ans) {
                //cout << ans << '\n';
                break;
            }
        }
    }
    cout << ans << '\n';
}



int main(){
    //freopen("in.txt","r",stdin);
    ios_base::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);
    int t(1), tcase(0); cin>>t;
    while (++tcase, t--) solve();
}