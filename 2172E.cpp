void solve(){
  //soLuSHoNN hErE.........
  string n;
  int j, k;
  cin >> n >> j >> k;
  
  string s ( n);
  sort(s.begin(), s.end());
  vector<string> perms;

  do {
      perms.push_back(s);
  } while (next_permutation(s.begin(), s.end()));

  string a = perms[j-1];
  string b = perms[k-1];

  int A(0);
  for (size_t i = 0; i < a.size(); ++i) {
    if (a[i] == b[i]) 
        ++A;
  }
  vector<int> fa(10,0), fb(10,0);

  for (char c : a) 
    fa[c - '0']++;
  for (char c : b) 
    fb[c - '0']++;

  int t0T(0);
  for (int d = 0; d <= 9; ++d) t0T += min(fa[d], fb[d]);
  int B = t0T - A;
  cout << A << 'A' << B << 'B' << '\n';
}

