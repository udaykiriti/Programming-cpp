void solve(){
   //soLuSHoNN hErE.........
   int n; cin >> n;
    vector<int> vec(n+1), knyc(n+1);
    for(int i=1;i<=n;i++){
        cin >> vec[i];
        knyc[vec[i]] = i;
    }

    vector<int> pm(n+1), smx(n+2);

    pm[1] = knyc[1];

    for(int i=2;i<=n;i++) {
        pm[i] = (pm[i-1] < knyc[i] ? pm[i-1] : knyc[i]);
    }

    smx[n] = knyc[n];

    for(int i=n-1;i>=1;i--) {
        smx[i] = (smx[i+1] > knyc[i] ? smx[i+1] : knyc[i]); 
    }
    bool ok(true);

    for(int k=1;k < n;k++){
        ok = (pm[k] > smx[k+1] ? false : ok);
    }
    cout << (ok ? "Yes" : "No") << '\n';
}

