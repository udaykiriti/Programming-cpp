#define pb push_back
#define vi vector<int>

void dfs(int u, string &s,vi &ans, vector<vi> a){

    if(a[u].size() == 0) {
        ans[u] = (s[u-1] == 'W' ? 1 : -1);
        return;
    }

    int sum{0};
    for(int i{0} ; i < a[u].size() ; i++){
        dfs(a[u][i] , s , ans , a);
        sum += ans[a[u][i]];
    }

    ans[u] = sum + (s[u-1] == 'W' ? 1 : -1);
}

void solve(){
   //soLuSHoNN hErE.........
    long long n;
    cin >> n;
    vector<vi> a(n+1);

    for(int i{2} ; i <= n ; i++) {
        int k;
        cin >> k;
        a[k].pb(i);
    }
    string s;
    cin >> s;
    vi ans(n+1, -1);
    dfs(1 , s , ans , a);
    int cnt{0};
    for(int i{1} ; i <= n ; i++) {
        if(0 == ans[i])
            cnt++;
    }
    cout << cnt << "\n";
}

int main(){
    //freopen("in.txt","r",stdin);
    ios_base::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);
    int t{1}, tcase{0}; cin>>t;
    while (++tcase, t--) solve();
}
