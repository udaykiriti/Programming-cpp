using int64 = long long;
const int64 LIMIT = 1000000000000LL; 

void solve(){
   //soLuSHoNN hErE.........
    int64 x, y, k;
    cin >> x >> y >> k;

    auto survivors = [&](int64 m)->int64{
        int64 cur = m;
        for(int64 i = 0; i < x && cur > 0; ++i){
            cur -= cur / y;
        }
        return cur;
    };

    if(survivors(LIMIT) < k){
        cout << -1 << '\n';
        return;
    }

    int64 lo = 1, hi = LIMIT, ans = -1;
    while(lo <= hi){
        int64 mid = lo + ((hi - lo) >> 1);
        if(survivors(mid) >= k){
            ans = mid;
            hi = mid - 1;
        } else {
            lo = mid + 1;
        }
    }
    cout << ans << '\n';
}

