#define ll long long

void solve(){
   //soLuSHoNN hErE.........
   int n;
   cin >> n;
   vector<ll> vec(n);
   for(auto &x : vec) cin >> x;
   int res(0);

   bool lk = (vec[0] != -1);
   bool rk = (vec[n-1] != -1);

   if( lk and rk ){
    res = llabs(vec[n-1] - vec[0]);
   }else{
    res = 0;
    if(!lk and rk) vec[0] = vec[n-1];
    if(lk and !rk) vec[n-1] = vec[0];
    if(!lk and !rk) {
        vec[0]=0;
        vec[n-1] = 0;
    }
   }

   for (ll i = 0; i < n; i++){
        vec[i] = (vec[i] == -1 ? 0 : vec[i]);
   }
   cout << res << '\n';

   for (ll i = 0; i < n; i++){
    cout << (i ? " " : "") << vec[i];
    }
  cout << endl;
}

