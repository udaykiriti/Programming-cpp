#define vi vector<int>
#define ll long long
#define pb push_back

void solve(){
   //soLuSHoNN hErE.........
    int n;
    cin >> n;
    vi Tp(n), Dn(n);
    for (int i = 0; i < n; ++i) cin >> Tp[i];
    for (int i = 0; i < n; ++i) cin >> Dn[i];

    vi pmxt(n), pmnt(n);
    pmxt[0] = Tp[0];
    pmnt[0] = Tp[0];
    for (int i = 1; i < n; ++i) {
        pmxt[i] = max(pmxt[i - 1], Tp[i]);
        pmnt[i] = min(pmnt[i - 1], Tp[i]);
    }

    vi smxb(n), smnb(n);
    smxb[n - 1] = Dn[n - 1];
    smnb[n - 1] = Dn[n - 1];
    for (int i = n - 2; i >= 0; --i) {
        smxb[i] = max(smxb[i + 1], Dn[i]);
        smnb[i] = min(smnb[i + 1], Dn[i]);
    }

    vi Mn(n), Mx(n);
    for (int j = 0; j < n; ++j) {
        Mn[j] = min(pmnt[j], smnb[j]);
        Mx[j] = max(pmxt[j], smxb[j]);
    }

    int U = 2 * n;
    vector<vi> bckts(U + 2);
    for (int j = 0; j < n; ++j) {
        int mn = Mn[j];
        if (mn < 1) mn = 1;
        if (mn > U) mn = U;
        bckts[mn].pb(Mx[j]);
    }

    ll ans = 0;
    priority_queue<int, vi, greater<int>> pq;
    for (int i = U; i >= 1; --i) {
        for (int val : bckts[i]) pq.push(val);
        if (!pq.empty()) {
            int r0 = pq.top();
            int rst = max(r0, i);
            if (rst <= U) ans += (ll)(U - rst + 1);
        }
    }
    cout << ans << '\n';
}

int main(){
    //freopen("in.txt","r",stdin);
    ios_base::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);
    int t(1), tcase(0); cin>>t;
    while (++tcase, t--) solve();
}