using ll = long long;

void solve(){
   //soLuSHoNN hErE.........
    ll n, c, d;
    cin >> n >> c >> d;

    map<ll, ll> freq;
    for (ll i = 0; i < n * n; ++i) {
        ll x;
        cin >> x;
        freq[x]++;
    }

    ll start = freq.begin()->first; 
    freq[start]--;

    for (ll i = 0; i < n; ++i) {
        ll current = start;
        for (ll j = 0; j < n - 1; ++j) {
            if (freq[current + d] == 0) {
                cout << "NO\n";
                return;
            }
            freq[current + d]--;
            current += d;
        }

        if (i < n - 1) {
            if (freq[start + c] == 0) {
                cout << "NO\n";
                return;
            }
            freq[start + c]--;
            start += c;
        }
    }

    cout << "YES\n";
}

