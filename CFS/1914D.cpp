void solve(){
   //soLuTioN hEre.........
   int n;
   cin >> n;
   vector <pair <ll,ll>> veca,vecb,vecc;
   ll x;

   for(int i = 0 ; i < n ; i++){
     cin >> x;
     veca.pb(make_pair(x,i));
   }

   debug(veca);
   for(int  i =0;i<n;i++){
     cin >> x;
     vecb.pb(make_pair(x,i));
   }

   debug(vecb);
   for(int i = 0; i < n ; i++){
     cin >> x;
     vecc.pb(make_pair(x,i));
   }
   debug(vecc);
   ll res = 0;

   sort(veca.begin(),veca.end(),greater<>());
   sort(vecb.begin(),vecb.end(),greater<>());
   sort(vecc.begin(),vecc.end(),greater<>());

   for(int i = 0 ; i < 3 ; i++){
     for(int j = 0 ; j < 3 ; j++){
       for(int k = 0 ;k < 3 ; k++){
         if(veca[i].second != vecb[j].second && veca[i].second != vecc[k].second && vecb[j].second != vecc[k].second)
           res = max(veca[i].first + vecb[j].first + vecc[k].first , res);
       }
     }
   }
  //  debug(veca);
  //  debug(vecb);
  //  debug(vecc);
   cout << res << '\n';
}

