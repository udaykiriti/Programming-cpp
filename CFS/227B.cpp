void solve(){
   //soLuSHoNN hErE.........
    int n;
    if (!(cin >> n)) return ;
    vector<int> pos(n + 1);
    for (int i = 1; i <= n; ++i) {
        int x; cin >> x;
        pos[x] = i;
    }

    int m; cin >> m;
    long long leftSum = 0, rightSum = 0;
    for (int i = 0; i < m; ++i) {
        int q; cin >> q;
        int p = pos[q];
        leftSum += p;              
        rightSum += (n - p + 1);   
    }

    cout << leftSum << ' ' << rightSum << '\n';
    return ;
}

