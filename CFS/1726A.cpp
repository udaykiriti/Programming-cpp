#include<bits/stdc++.h>
using namespace  std;

void solve(){
  int n;
  cin >> n;
  vector<int> vec(n);
  
  for(auto &x : vec) cin >> x;
  int res(0);

  for (int i = 0; i < n-1; i++) {
    res = max(res , vec[i] - vec[i+1]);
  }

  for(int i = 0 ; i < n - 1 ; i++){
    res = max(res, vec[n-1] - vec[i]);
  }

  for(int i = 0 ; i < n ; i++){
    res = max(res , vec[i] - vec[0]);
  }
  cout << res << '\n';
}

int main (int argc, char *argv[]) {
  ios_base::sync_with_stdio(0);
  cin.tie(0);
  cout.tie(0);

  int t(1),tcase(0);
  cin >> t;
  while (++tcase,t--) {
    solve();
  }
  return 0;
}
