int nb, ns, nc, pb, ps, pc, r, ans = 0;
int b = 0, s = 0, c = 0;

void check(int left, int right) {
    int bb, ss, cc;
    if (ans != 0) return;
    if (left < right) {
        int mid = (left + right + 1) >> 1;
        bb = max(0LL, mid * b - nb);
        ss = max(0LL, mid * s - ns);
        cc = max(0LL, mid * c - nc);
        if (bb * pb + ss * ps + cc * pc <= r)
            check(mid, right);
        else
            check(left, mid - 1);
    } else ans = left;
}

void solve() {
    string str;
    cin >> str;
    cin >> nb >> ns >> nc;
    cin >> pb >> ps >> pc;
    cin >> r;

    for (char ch : str) {
        if (ch == 'B') b++;
        else if (ch == 'S') s++;
        else c++;
    }
    debug(c,s,b);
    int left = 0, right = 1e14;
    check(left, right);
    cout << ans << "\n";
}

