void solve()
{
    // soLuTioN hEre....
    int n;
    cin >> n;
    vi ans(n);
    
    for (int i = 0; i < n; i += 2)
        ans[i] = -1;

    int idx = n - 1;
    if (n & 1)
    {
        idx--;
        while (idx > 0)
        {
            ans[idx] = 3;
            idx -= 2;
        }
    }
    else
    {
        ans[idx] = 2;
        idx -= 2;
        while (idx > 0)
        {
            ans[idx] = 3;
            idx -= 2;
        }
    }
    debug(ans);
    for (int i = 0; i < n; i++)
        cout << ans[i] << " ";
    cout << endl;
}

