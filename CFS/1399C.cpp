void solve() {
    //soLuSHoNN hErE.........
    int i, j, n, res = 0, cur;
    ll ar[1001];
    ll w[1001];

    ll toT = 200, ans = 0, cnt;
    res = 0;

    toT = 200;
    while (toT--) ar[toT] = 0;

    if (!(cin >> n)) return;
    for (i = 0; i < n; i++) cin >> w[i];
    sort(w, w + n);

    for (ll k = 1; k < 101; k++) {
        i = 0, j = n - 1, cnt = 0;
        while (i < j) {
            if (w[i] + w[j] == k) {
                i++; j--;
                cnt++;
            } else if (w[i] + w[j] < k) i++;
            else j--;
        }
        if (ans < cnt) ans = cnt;
    }
    cout << ans << "\n";
}


