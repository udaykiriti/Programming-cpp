void solve(){
   //solution here.........
    int k;
    string s;
    cin >> k >> s;

    vector<int> freq(26, 0);
    for (char c : s) freq[c - 'a']++;

    string t = "";
    for (int i = 0; i < 26; i++) {
        if (freq[i] % k != 0) {
            cout << -1;
            return ;
        }
        t += string(freq[i] / k, 'a' + i);
    }

    string ans = "";
    for (int i = 0; i < k; i++) ans += t;
    cout << ans << flush;
}

