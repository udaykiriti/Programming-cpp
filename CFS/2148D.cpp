void solve() {
    ll n;
    cin >> n;
    vector<ll> a(n), b;
    ll evensum = 0;

    for (int i = 0; i < n; i++) {
        cin >> a[i];
        if (a[i] % 2 == 0)
            evensum += a[i];
        else
            b.push_back(a[i]);
    }

    if (b.empty()) {
        cout << 0 << endl;
        return;
    }

    sort(b.begin(),b.end());
    ll noo = evensum;
    for (size_t i = 0; i < (b.size() + 1) / 2; i++){
        noo += b[b.size() - 1 - i];
    }

    cout << noo << endl;
}



