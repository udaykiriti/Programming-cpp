#define ll long long
#define vll vector<ll>
#define pb push_back

void solve(){
   //soLuSHoNN hErE.........
   int n;
   ll l,r;
   cin >> n >> l >> r;
   vll vec(n);
   for(int i = 0; i < n ;i++){
     cin >> vec[i];
   }
   debug(vec);

   if(l == r){
    ll res = 0;
    for(ll i = 0 ;i < (ll)n ; i++ ){
        res += llabs(vec[i] -l);
    }

    cout << res << '\n';
    return ;
   }

   vll lr,rr,mr;
   
   for (ll i = 0; i < n; i++) {
    if (vec[i] <= l) {
        lr.pb(vec[i]);
    } else if (vec[i] >= r) {
        rr.pb(vec[i]);
    } else {
        mr.pb(vec[i]);
    }
    }


   ll lbst(0),rbst(0);

   for(size_t i = 0; i< lr.size();i++){
    lbst += l - lr[i];
    rbst += r - lr[i];
   }

   for(size_t i = 0; i< rr.size() ;i++){
    lbst += rr[i] - l;
    rbst += rr[i] - r;
   }


   sort(mr.begin(),mr.end());
   debug(mr);
   int sz = mr.size();

   vll pre(sz + 1);
   debug(pre);
   for(int i = 0 ;i < sz; i++){
    pre[i+1] = pre[i] + mr[i];
   }

   ll toT = pre[sz];

   ll ans(0);

   for(int  i= 0; i <= sz ; i++){
    double tmp = i + double(lbst - rbst ) / double(l -r);
    int y = floor(tmp);

    int cand[] = {y-2,y-1,y,y+1,y+2,0,sz - i};
    for(int s : cand){
        if(s < 0 or s > sz - i) continue;
        ll AA = pre[i];
        ll BB = toT - pre[sz - s];
        ll ltoT = lbst + (ll)i * l - AA +BB - (ll)s*l;
        ll rtoT = rbst + (ll)i * r -AA + BB - (ll)s*r ;
        ans  = max(ans, min(rtoT,ltoT));  
    }
   }
   cout << ans << '\n';
}


int main(){
    //freopen("in.txt","r",stdin);
    ios_base::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);
    int t(1), tcase(0); cin>>t;
    while (++tcase, t--) solve();
}