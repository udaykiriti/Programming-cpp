void solve(){
   //solution here.........
  ll n, m; cin >> n >> m;
  vi v(n);
  for(ll p = 0; p < n; p++) cin >> v[p];
  sort(v.rbegin(), v.rend());
  ll total(0);
  for(ll p = 0; p < n && p < m; p++){
    total += (m - p) * v[p];
  }
  cout << total << '\n';
}

