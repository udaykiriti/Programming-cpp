using ll = long long;
#define FOR(i,n) for(int i = 1; i <= (n); ++i)
using vll = vector<ll>;

void solve(){
    //soLuSHoNN hErE.........
    int n; 
    cin >> n;
    vll vec(n+1), pr(n+1);
    FOR(i,n) { 
        cin >> vec[i];

        pr[i] = pr[i-1] + vec[i]; 
    }
    
    ll ans = pr[n], bt = LLONG_MIN, mxi = 0;

    FOR(p,n) {
        ll cd = pr[p-1] + p - 1LL * p * p;
        bt = (cd > bt ? cd : bt);
        
        ll dt = 1LL* p *(p+1) - pr[p] + bt;

        mxi = (dt > mxi ? dt : mxi);
    }
    cout << ans + max(0LL, mxi) << '\n';
}

