#define pb push_back
const int maxn = 200010;
#define vi vector<int>

int n, m, k, a[maxn * 2], cnt;
bool vis[maxn];
vi G[maxn];

void dfs(int u) {
    vis[u] = 1;
    a[++ cnt] = u;
    for (auto v : G[u]) if (!vis[v]) {
        dfs(v);
        a[++cnt] = u;
    }
}

int main() {
    ios :: sync_with_stdio(false);
    cin.tie(nullptr), cout.tie(nullptr);
    cin >> n >> m >> k;
    for (int i = 1; i <= m; i ++) {
        int u, v;
        cin >> u >> v;
        G[u].pb(v);
        G[v].pb(u);
    }
    int lmt = (2 * n + k - 1) / k;
    vector < vector < int > > res(k, vector < int > (0));
    dfs(1);
    
    int curr = 1;

    for (int i = 0; i < k; i ++) {
        while (res[i].size() < lmt and curr <= cnt) 
            res[i].pb(a[curr ++]);
    }

    for (int i = 0; i < k; i ++) {
        cout << res[i].size() << " ";
        for (auto j : res[i]) 
            cout << j << " "; 
        cout << '\n';
    }
    return 0;
}