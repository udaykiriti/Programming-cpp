#define pb push_back
#define all(x) x.begin(), x.end()

ll n;
ll a[maxn];

ll obtain(ll x) {
    unordered_set<ll> st;
    ll c = 0, ans = 1;
    for (ll i = 1; i <= n; i++) {
        if (!st.count(a[i])) {
            if (c == x) {
                ans++;
                c = 0;
                st.clear();
            }
            c++;
            st.insert(a[i]);
        }
    }
    return ans;
}

void solve() {
    //soLuSHoNN hErE.........
    cin >> n;

    for (ll i = 1; i <= n; i++)
        cin >> a[i];

    for (ll k = 1; k <= min(n, sq); k++)
        cout << obtain(k) << ' ';

    for (ll k = sq + 1; k <= n;) {
        ll x = obtain(k);
        ll l = k, r = n + 1;
        while (r - l > 1) {
            ll m = (l + r) / 2;
            if (obtain(m) == x)
              l = m;
            else
              r = m;
        }
        for (ll j = k; j < r; j++) {
            cout << x << ' ';
            k++;
        }
    }
    cout << '\n';
}

