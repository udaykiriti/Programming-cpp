void solve(){
   //soLuSHoNN hErE.........
   int n,m;
   cin >> n >> m;
   string s;
   cin >> s;
   s = "#" + s;
   const int len = 6*n + 6*m + 100;
   vector<ll> st(len + 5);
   vector<ll> t;

   for(ll i = 1 ; i <= m; i++){
     ll x;
     cin >> x;
     if(x <= len)
       st[x] = 1;
     else
       t.push_back(x);
   }
   debug(st);
   debug(t);
   int p(1);

   for(ll i = 1 ; i <= n ;i++){
     if(s[i] == 'A'){
       p++;
       st[p] = 1;
     }else{
       int q = p;
       while(st[q + 1])
         q++;
       q++;
       st[q] = 1;
       while(st[q+1])
         q++;
       q++;
       p = q;
     }
   }

   vector <ll> ans;

   for(ll i = 1 ; i <= len +1 ;i++){
     if(st[i])
       ans.push_back(i);
   }

   cout << ans.size() + t.size() << '\n';

   for(ll x : ans){
     cout << x << ' ';
   }
   for(ll x : t){
     cout << x << ' ';
   }
   cout << endl;
}

